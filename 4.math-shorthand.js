// Sum Shorthand Calculation
var price1 = 75;
price1 = price1 +1;
price1 += 1;
price1++;
console.log(price1);


// Subtraction Shorthand Calculation
var price1 = 75;
price1 = price1 -1;
price1 -= 1;
price1--;
console.log(price1);


// Multiplication Shorthand Calculation
var price1 = 75;
price1 = price1 *5;
price1 *= 2;
console.log(price1);


// Division Shorthand Calculation
var price1 = 75;
price1 = price1 /5;
price1 /= 2;
console.log(price1);